package com.boeing.training.flightticketsystem.model;


public class UserData {
	
	private String way;
	private String from;
	private String to;
	private int adults;
	private String departOn;
	private String returnOn;
	public String getWay() {
		return way;
	}
	public void setWay(String way) {
		this.way = way;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public int getAdults() {
		return adults;
	}
	public void setAdults(int adults) {
		this.adults = adults;
	}
	public String getDepartOn() {
		return departOn;
	}
	public void setDepartOn(String departOn) {
		this.departOn = departOn;
	}
	public String getReturnOn() {
		return returnOn;
	}
	public void setReturnOn(String returnOn) {
		this.returnOn = returnOn;
	}
	
	
	}
